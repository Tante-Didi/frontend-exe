// type Identification
const num = 10;
console.log(typeof num); //number

const str = "Hello";
console.log(typeof str); //string

const bool = true;
console.log(typeof bool); //boolean

// Comparing Variable Types
const firstVar = 42;
console.log(typeof firstVar); //number
const secondVar = 42;
console.log(typeof secondVar); //number

//Undefined and Null Type
let noValue;
console.log(typeof noValue); //undefined
let emptyValue = null;
console.log(typeof emptyValue); //object

